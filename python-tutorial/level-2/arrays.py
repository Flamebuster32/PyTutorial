# an array is a *SPECIAL* variable that can hold multiple variables
ara = []
ara.append("o")  # .append adds a value to an array
ara.pop(0)  # pop removes an item from a list
ara.append("w")
ara.append("o")
ara.remove("o")  # .remove like .pop removes an item from the list, except .pop takes the place of the item in the list
# while .remove takes the item itself

len(ara)  # len finds out a length of an array, list, string, etc.
